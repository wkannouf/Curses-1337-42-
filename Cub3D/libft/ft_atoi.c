#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	size_t	result;
	size_t	b_result;
	short	sign;

	i = 0;
	result = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		b_result = result;
		result = result * 10 + str[i++] - 48;
		if (result > 2147483647)
			return (-1);
	}
	return (result * sign);
}
