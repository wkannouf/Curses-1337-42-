#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (fd < 0 || !s)
		return ;
	if (fd == 2)
		write(fd, "\033[1;31m", 7);
	write(fd, s, ft_strlen(s));
	if (fd == 2)
		write(fd, "\033[0m", 4);
}
